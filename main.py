# Register new users into the User database
@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():

        # Check if the email entered is already registered
        if request.form.get('email'):
            flash('You are already registered, you can go to the account', category='error')
            return redirect(url_for('login'))

        # Hash and salt the entered password
        hash_and_salted_password = generate_password_hash(
            form.password.data,
            method='pbkdf2:sha256',
            salt_length=8
        )

        # Create a new User object with the entered email, name, and password
        new_user = User(
            email=form.email.data,
            name=form.name.data,
            password=hash_and_salted_password,
        )

        # Add the new User object to the database and commit the changes
        db.session.add(new_user)
        db.session.commit()

        # Redirect the user to the secrets page
        return redirect(url_for("login"))

    # If the request method is not POST, render the register template
    return render_template("register.html", form=form)


