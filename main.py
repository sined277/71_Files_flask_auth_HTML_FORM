@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        email = form.email.data
        password = form.password.data

        # Find the user with the entered email in the database
        user = User.query.filter_by(email=email).first()

        # Check if the entered password matches the stored password hash for the user
        if user and check_password_hash(user.password, password):
            # Log in and authenticate the user
            login_user(user)
            # Redirect the user to the secrets page
            return redirect(url_for('get_all_posts'))
        elif not user:
            # If the email is not registered, flash an error message and redirect to the login page
            flash('The email is not exist, please try again!', category='error')
            return redirect(url_for('login'))
        elif not check_password_hash(user.password, password):
            flash('Password incorrect, please try again!', category='error')
            return redirect(url_for('login'))

    return render_template("login.html", form=form)


